#include "libft.h"

void	ft_putnbr_fd(int c, int fd)
{
	if (c == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
	}
	else if (c < 0)
	{
		ft_putchar_fd('-', fd);
		c = c * -1;
	}
	if (c >= 10)
		ft_putnbr_fd(c / 10, fd);
	ft_putchar_fd((c % 10) + '0', fd);
}
