int	is_space(char c)
{
	if ((c == ' ') || (c == '\t') || (c == '\v') || (c == '+'))
		return (1);
	else if ((c == '\f') || (c == '\n') || (c == '\r') || (c == '0'))
			return (1);
		else
			return (0);
}
